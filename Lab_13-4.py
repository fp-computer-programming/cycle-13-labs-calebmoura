# List called day_date which holds the number of day starting from the second.
day_date = [2, 3, 4, 5, 6]

# 5 letters assigned from list to a variable which represents each day using a single statement using tuple.
thu, fri, mon, tue, wed = tuple(day_date)

# Each of variables printed (thu, fri, mon, tue, wed) to show what they are equal to.
print("Thursday:", thu)
print("Friday:", fri)
print("Monday:", mon)
print("Tuesday:", tue)
print("Wednesday:", wed)